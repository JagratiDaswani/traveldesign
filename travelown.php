<p>You’re excited to be heading to that exotic locale for your upcoming vacation,
     but in order to have a carefree time, it’s wise to invest a bit of time
      upfront to make sure you’ve covered your bases.

Follow this checklist to ensure that your 
finances are in order, you’ve packed appropriately
 and your travel plans will go off without a hitch.

Plus, find tips on how to use your NEA member benefits 
to find travel discounts for teachers and other public-school professionals.</p>

                  <h2>
                1. Make sure your passport and visa are up-to-date</h2>
                <p>Most countries require that your U.S. passport be valid for six months after your return date.
 The State Department recommends that you renew your passport no less than nine months
  before it’s set to expire.

Check the expiration date of your passport now,
 and if you need to renew, check the U.S. Department of State’s website
  to find your nearest passport facility.

Allow 10 to 12 weeks for the passport application to be processed.
 If you need the passport faster than that, use the expedited service 
 for an extra fee and receive your passport in four to six weeks.
  Private expediting services can get it done more quickly
  , but they charge much higher fees.

The State Department’s country information updates provide important facts about your destination, such as whether you need a visa and where to get it, as well as other important details on crime, special circumstances, medical information and more.

If you plan to rent a car or drive at your destination, find out whether an international driver’s license is required or if your U.S. license will suffice.

Make a photocopy of the information page of your passport and the visa page(s) 
for your destination(s). Pack them separately from your passport.</p>

<h2>  2. Check for health advisories and travel warnings and advisories</h2>
<p>Check the State Department’s website to find out whether the U.S. government has issued a travel warning, for countries where long-term issues create a risky environment for travelers, or a travel alert, for countries with short-term conditions that may pose a threat to travelers, for your destination. Many travel insurance policies will not cover travel to countries that are under travel warnings.

The continuing impact of the SARS-CoV-2 coronavirus (which causes the COVID-19 disease) 
is still unfolding and is affecting different countries across the globe in different ways.
 Before your next trip, check the Centers for Disease Control
  and Prevention’s website for the most current information,
   including updates on COVID-19 testing and vaccinations that may be required 
   for international travel.</p>

<h2>  3. Get vaccinations and fill prescriptions</h2>

<p>Check with the CDC to find out health information and vaccination requirements for your destination. That can include vaccines for other diseases such as cholera, yellow fever and Zika virus. For maximum protection, you should get any required shots six weeks before departure. If you’re traveling to a country where disease is rampant, consult a travel medicine professional for expert personalized guidance.

The CDC website is also a good source of information on the health risks of consuming the water and food at your destination, any non-prescription items to pack and other health tips.

Be sure to check the foreign embassy of the country you’re traveling to, or passing through, to learn which prescription drugs are permitted or illegal in the country.

Bring photocopies of your prescriptions, packed separately from your medicine, and note the generic drug name. Always pack your medicine, in its prescription bottles, in your carry-on bag. If you need to use syringes, make sure that you have a letter from your doctor. Always declare the syringes before you go through security.

Pack more than enough medicine to last your whole trip,
 as it may be difficult to get your prescription filled abroad.</p>

<h2>4. Register your trip with the State Department</h2>
<p>If you’re traveling outside industrial countries or to remote areas,
 consider registering online with the U.S. Department of State, and enter your 
 itinerary. In case of an emergency, the U.S. government will know about your
  presence in the country and where to contact you. Also, if you specify that your travel 
  information can be shared with third parties, your family and friends can contact 
  the Department of State to locate you if necessary. Registration is free.</p>

<h2>   5. Stock your wallet with acceptable methods of payment</h2>
<p>Choose one or two credit cards to take with you, and call the issuers shortly before you leave to tell them which countries you’ll be visiting. Otherwise, your credit card could be denied, as the issuer may find the international activity suspicious. Reduce fraud risk by leaving behind any cards you don’t plan to use on your trip.

Traveler’s checks are no longer universally accepted, and you may have trouble using them in many countries, especially developing ones. Instead, use your bank card to withdraw money from ATMs, which can be found in even the most exotic destinations. Memorize your numeric PIN, as many ATM keypads will not display the numbers and letters we use, or they may be placed differently on the keypad. 

Your most favorable exchange rate generally will be through the ATM, although most companies will charge a transaction fee.

Write down your credit card numbers, but don’t keep the list in your wallet. Know how to contact your credit card company from abroad. Toll-free numbers don’t work from outside the U.S., but credit card companies will accept collect calls at a designated number. 

Get more tips on how to protect your credit while traveling.</p>

<h2>  6. Buy travel insurance</h2>
<p>It’s likely that your health insurance won’t cover you while you’re traveling abroad. Depending on where you’re going, consider buying medical evacuation and emergency medical insurance. Also, if your trip requires a large down payment or is booked many months in advance, trip interruption and cancellation insurance can provide valuable protection from the unexpected. 

Read Travel Insurance 101 to learn more about types of travel insurance coverage.</p>

<h2>    7. Pack appropriately</h2>
<p>Other than the necessary electronics, don’t bring valuables with you. Expensive jewelry could attract thieves who are experts at targeting tourists. If you do bring valuables, always travel with them in your carry-on, or keep them locked up in the hotel safe.

If you wear prescription glasses, pack an extra pair.

Use closed baggage tags to label each bag. Put your name, address and phone number both inside and outside your luggage.

Buy Transportation Security Administration-approved locks, and always keep your luggage locked. Check to see if you can send your locked bags on in-country flights. TSA locks may not be recognized and could be cut to inspect your bags. 

Never put valuables in your checked luggage, and check carefully after going through security to ensure that all of your valuables are still in your carry-on. 

Always do your homework before you travel to another country
As educators, you know that studying up is the best way to be prepared. Do your research. The more you know about the history and customs of your destination countries, the more you will enjoy and benefit from your journey. Here are some examples to get you started:

Don’t bring shorts, miniskirts and tank tops if you’re going to a conservative country.
If you’re going to a place where you will have to take your shoes off frequently to enter tourist sites, pack slip-on shoes.
Know whom to tip and how much.
Learn how to say simple phrases in the language of your destination, such as “thank you” and “good morning.”</p>
